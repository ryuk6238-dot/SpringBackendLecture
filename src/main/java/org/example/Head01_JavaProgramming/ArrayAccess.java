package org.example.Head01_JavaProgramming;

public class ArrayAccess {
    public static void main(String[] args) {
        int[] scores = {88,92,76};
        System.out.println(scores[0]);
        scores[1] = 95;
        scores[2] = 105;
        System.out.println(scores[1]);
        System.out.println(scores[2]);

    }

}
